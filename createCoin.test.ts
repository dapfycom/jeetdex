const factoryConfig = new TransactionsFactoryConfig({ chainID: 'D' });

const createBackendCoin = async ({
  description,
  title,
  userAddress,
  img
}: {
  description: string;
  title: string;
  userAddress: string;
  img: string;
}) => {
  const degenId = generateRandomString(10);
  try {
    const res = await prisma.coins.create({
      data: {
        identifier: degenId,
        degenId,
        description,
        img,
        owner: {
          connect: {
            address: userAddress
          }
        },
        title
      }
    });

    return res;
  } catch (error) {
    console.log(error);
    return null;
  }
};

const newCoinTx = async (
  {
    name,
    ticker,
    degenId
  }: {
    name: string;
    ticker: string;
    degenId: string;
  },
  senderAddress: string
) => {
  const factory = new SmartContractTransactionsFactory({
    config: factoryConfig
  });

  const args: any[] = [
    BytesValue.fromUTF8(name),
    BytesValue.fromUTF8(ticker.toUpperCase()),
    BytesValue.fromUTF8(degenId),
    new BooleanValue(false)
  ];

  const transaction = factory.createTransactionForExecute({
    sender: Address.fromBech32(senderAddress),
    contract: Address.fromBech32(scAddress.degenMaster),
    function: 'newToken',
    gasLimit: BigInt(500000000),
    arguments: args,
    nativeTransferAmount: BigInt(
      new BigNumber(0.123).multipliedBy(10 ** 18).toFixed(0)
    )
  });
  const alice = new Account(Address.fromBech32(senderAddress));
  const aliceOnNetwork = await provider.getAccount(
    Address.fromBech32(senderAddress)
  );
  alice.update(aliceOnNetwork);
  transaction.nonce = alice.getNonceThenIncrement() as any;

  console.log('transaction');
  console.log(transaction);

  return transaction;
};

const signTx = async (signer: UserSigner, tx: Transaction) => {
  const computer = new TransactionComputer();
  const serializedTx: any = computer.computeBytesForSigning(tx);

  tx.signature = await signer.sign(serializedTx);
  return tx;
};

const createScCoin = async (
  signer: UserSigner,
  { degenId, name, ticker }: { degenId: string; name: string; ticker: string }
) => {
  const tx = await newCoinTx(
    {
      degenId,
      name,
      ticker
    },
    signer.getAddress().bech32()
  );
  console.log('siginig tx');

  const signedTx = await signTx(signer, tx);

  const txHash = await provider.sendTransaction(signedTx);
  console.log(txHash);

  return tx;
};

